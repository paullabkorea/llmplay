/**
 * 대화에 쌓인 토큰 수 → AI의 '집중력'(%).
 * 실제 성능 곡선이 아니라 "길수록 둔해진다"를 보여 주는 교육용 곡선.
 */
export function focusFor(ctxTokens) {
  const over = Math.max(0, ctxTokens - 1500);
  return Math.round(Math.min(100, Math.max(30, 100 - (over * 60) / 10000)));
}

/** 누적 토큰 → 상태 메시지와 게이지 색 단계('ok' | 'warn' | 'bad') */
export function statusFor(ctxTokens) {
  if (ctxTokens === 0) {
    return { level: 'ok', msg: '메시지를 보내면 토큰이 쌓이기 시작해요.' };
  }
  if (ctxTokens < 2000) {
    return { level: 'ok', msg: '아직 가벼워요. AI가 대화 전체를 쌩쌩하게 다시 읽어요.' };
  }
  if (ctxTokens < 6000) {
    return { level: 'ok', msg: '대화가 쌓이고 있어요. 매번 이만큼을 통째로 다시 읽는 중!' };
  }
  if (ctxTokens < TOKEN_LIMIT_HINT) {
    return { level: 'warn', msg: '슬슬 무거워요. 답이 느려지고, 앞부분 내용을 놓치기 시작해요.' };
  }
  return { level: 'bad', msg: '1만 토큰을 넘었어요! 지금부터는 새 대화를 여는 게 유리해요.' };
}
