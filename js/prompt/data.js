export const BUILD_QUESTION = '여행 계획 좀 짜 줘.';

/** 아무 재료도 없을 때 원하는 답이 나올 확률(%) */
export const BUILD_BASE = 15;

/**
 * 얹을 수 있는 재료들. gain은 이 재료가 끌어올리는 확률(%p).
 * 전부 더해도 100%가 되지 않게 유지한다 — 확률 뽑기라는 본질은 그대로니까.
 */
